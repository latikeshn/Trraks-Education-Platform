//webinar time scheduled clock down code starts here..

 var countDownDate = new Date("april 14, 2018 15:17:00").getTime();

// Update the count down every 1 second
var x = setInterval(function() {

    // Get todays date and time
    var now = new Date().getTime();
    
    // Find the distance between now an the count down date
    var distance = countDownDate - now;
    
    // Time calculations for days, hours, minutes and seconds
    var days = Math.floor(distance / (1000 * 60 * 60 * 24));
    var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    var seconds = Math.floor((distance % (1000 * 60)) / 1000);
    
    // Output the result in an element with id="demo"
    document.getElementById("webinar-date-countdown").innerHTML ="<span style='font-size: 20px'> <span style='font-size:30px'> " + days + "</span>  Day  <span style='margin-left:03px;font-size:30px'> " + hours + " </span>Hr <span style='font-size:30px'>  : </span> <span style='font-size:30px'> "
    + minutes + "</span> Min <span style='font-size:30px'>  :  </span><span style='font-size:30px'> " + seconds + "</span> Sec </span>";

    // If the count down is over, write some text 
    if (distance < 0) {
        clearInterval(x);
        document.getElementById("webinar-date-countdown").innerHTML = "<i style='color:red;'>Webinar Started</i>";
    }
}, 1000);   
